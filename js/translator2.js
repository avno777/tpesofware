function updateLanguageLinks(lang) {
  const linksToUpdate = document.querySelectorAll("[data-lang-link]");
  linksToUpdate.forEach((linkElement) => {
    const page = linkElement.getAttribute("data-lang-link");
    linkElement.href = `${page}.html?lang=${lang}`;
  });
}

// Khi trang tải xong, áp dụng ngôn ngữ từ URL và cập nhật các liên kết
const urlParams = new URLSearchParams(window.location.search);
const selectedLang = urlParams.get("lang") || "en";
applyLanguage(selectedLang);
updateLanguageLinks(selectedLang);

const links = document.querySelectorAll("a");
links.forEach((link) => {
  const href = link.getAttribute("href");
  if (href && !href.startsWith("#") && !href.includes("lang=")) {
    link.setAttribute(
      "href",
      href + (href.includes("?") ? "&" : "?") + "lang=" + selectedLang
    );
  }
});
